
public class Vehicles {


    private String name;

    private int id;

    private double price;

    private int MPG;

    public void setName(String vehiclesNames) {

        name = vehiclesNames;

    }

    public void setId(int vehiclesId) {

        id = vehiclesId;

    }

    public void setPrice(double vehiclesPrice) {

        price = vehiclesPrice;

    }



    public void setMPG(int mpg) {

        MPG=mpg;

    }



    public int getMPG() {

        return MPG;

    }



    public double getPrice() {

        return price;



    }



    public String getName() {

        return name;

    }



    public int getID() {

        return id;

    }


}


